package com.chakulaconnect;

public class PostsModel {
    String postId, sourceId, type, photoUri, postTitle, date, description;

    public PostsModel(String postId, String sourceId, String type, String photoUri, String postTitle, String date, String description) {
        this.postId = postId;
        this.sourceId = sourceId;
        this.type = type;
        this.photoUri = photoUri;
        this.postTitle = postTitle;
        this.date = date;
        this.description = description;
    }

    public String getPostId() {
        return postId;
    }

    public void setPostId(String postId) {
        this.postId = postId;
    }

    public String getSourceId() {
        return sourceId;
    }

    public void setSourceId(String sourceId) {
        this.sourceId = sourceId;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getPhotoUri() {
        return photoUri;
    }

    public void setPhotoUri(String photoUri) {
        this.photoUri = photoUri;
    }

    public String getPostTitle() {
        return postTitle;
    }

    public void setPostTitle(String postTitle) {
        this.postTitle = postTitle;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
}
